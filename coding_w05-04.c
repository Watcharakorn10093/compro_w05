#include <stdio.h>

// สร้างโครงสร้างข้อมูล struct
struct Person {
char name; // ประกาศตัวแปรสำหรับเก็บชื่อเล่น
int age; // ประกาศตัวแปรสำหรับเก็บอายุ
float height; // ประกาศตัวแปรสำหรับเก็บส่วนสูง
float weight; // ประกาศตัวแปรสำหรับเก็บน้ำหนัก
char grade; // ประกาศตัวแปรสำหรับเก็บรหัสเกรด
};

int main() {
struct Person people[3]; // ประกาศอาเรย์สำหรับเก็บข้อมูลของ 3 คน

printf("=== รับข้อมูลจำนวน 3 คน ===\n"); // ข้อความแนะนำให้ผู้ใช้ป้อนข้อมูล
printf("ป้อนข้อมูลรูปแบบ: ชื่อเล่น อายุ ส่วนสูง น้ำหนัก รหัสเกรด\n"); // ข้อความแนะนำรูปแบบการป้อนข้อมูล
printf("ตัวอย่าง: A 21 175.5 90.2 A\n\n"); // ตัวอย่างการป้อนข้อมูล

// รับข้อมูลของแต่ละคน
for (int i = 0; i < 3; i++) { // วนลูปรับข้อมูล 3 คน
printf("ป้อนข้อมูลคนที่ %d: ", i + 1); // ข้อความให้ผู้ใช้ป้อนข้อมูล
// รับข้อมูลจากผู้ใช้ โดยใช้ %c สำหรับตัวอักษร, %d สำหรับจำนวนเต็ม, %f สำหรับจำนวนทศนิยม
// และใช้ & เพื่อส่งที่อยู่ของตัวแปร
scanf(" %c %d %f %f %c",
&people[i].name, // รับชื่อเล่น
&people[i].age, // รับอายุ
&people[i].height, // รับส่วนสูง
&people[i].weight, // รับน้ำหนัก
&people[i].grade); // รับรหัสเกรด
}

// แสดงผลในรูปแบบตาราง
printf("\n+-------+-----+-----------+-----------+------------+\n"); // เส้นขอบบนของตาราง
printf("| Name | Age | Height(cm)| Weight(kg)| Grade Code |\n"); // หัวตาราง
printf("+-------+-----+-----------+-----------+------------+\n"); // เส้นขอบกลางของตาราง

for (int i = 0; i < 3; i++) { // วนลูปแสดงข้อมูลของแต่ละคน
printf("| %c | %3d | %6.1f | %6.1f | %c |\n", // แสดงข้อมูลในรูปแบบตาราง
people[i].name, // แสดงชื่อเล่น
people[i].age, // แสดงอายุ
people[i].height, // แสดงส่วนสูง
people[i].weight, // แสดงน้ำหนัก
people[i].grade); // แสดงรหัสเกรด
}

printf("+-------+-----+-----------+-----------+------------+\n"); // เส้นขอบล่างของตาราง

return 0; // คืนค่า 0 เพื่อบ่งชี้ว่าการทำงานสำเร็จ
}